package com.config;

public class ClientConfig {

	private String funcNo;
	private String funcDesc;
	private String reqRespType;
	private String shcema;
	private String url;
	private String configId;
	
	public String getFuncNo() {
		return funcNo;
	}
	public void setFuncNo(String funcNo) {
		this.funcNo = funcNo;
	}
	public String getFuncDesc() {
		return funcDesc;
	}
	public void setFuncDesc(String funcDesc) {
		this.funcDesc = funcDesc;
	}
	public String getReqRespType() {
		return reqRespType;
	}
	public void setReqRespType(String reqRespType) {
		this.reqRespType = reqRespType;
	}
	public String getShcema() {
		return shcema;
	}
	public void setShcema(String shcema) {
		this.shcema = shcema;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getConfigId() {
		return configId;
	}
	public void setConfigId(String configId) {
		this.configId = configId;
	}
	
}
